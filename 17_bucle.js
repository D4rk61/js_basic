// Otro uso de for en js

console.clear()
console.log("\nCiclo for\n")
var contador = 0
for (var i = 10; i > contador; i--) {
    console.log(`La iteracion va por: ${i}`)
}

console.log("\nCiclo while\n")
var contador2 = 0

while (contador2 < 10+1) {
    console.log(`Numero de iteracion: ${contador2}`)
    contador2++
}